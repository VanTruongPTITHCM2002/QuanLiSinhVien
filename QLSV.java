    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
         try {
            if (!(txtmaSV.getText().equals(""))) {
                StudentDAO dao = new StudentDAO();     
                Student sv = dao.findByMaSV(txtmaSV.getText());
                
                  if(sv == null) {
                        JOptionPane.showMessageDialog(this, "Không tìm thấy mã sinh viên này", "Tìm kiếm thất bại", JOptionPane.ERROR_MESSAGE);
                  }
                  else{
                        txtmaSV.setText(sv.getMaSV());
                        txthoLot.setText(sv.getHoLot());
                        txtTen.setText(sv.getTenSV());
                        txtmaLop.setText(sv.getMaLop());
                        txtTenLop.setText(sv.getTenLop());
                        txtsoDT.setText(sv.getSoDienThoai());
                        txtEmail.setText(sv.getEmail());
                        btnCapNhat.setEnabled(true);
                        btnXoa.setEnabled(true);
                        
                  }
                
                
            } 
           
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_btnTimKiemActionPerformed


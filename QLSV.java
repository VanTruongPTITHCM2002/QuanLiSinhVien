    private void tableSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSVMouseClicked
        // TODO add your handling code here:
        int row = tableSV.getSelectedRow();
        if (row >= 0) {
            txtmaSV.setText(tableSV.getValueAt(row, 0).toString());
            txthoLot.setText(tableSV.getValueAt(row, 1).toString());
            txtTen.setText(tableSV.getValueAt(row, 2).toString());
            txtmaLop.setText(tableSV.getValueAt(row, 3).toString());
            txtTenLop.setText(tableSV.getValueAt(row, 4).toString());
            txtsoDT.setText(tableSV.getValueAt(row, 5).toString());
            txtEmail.setText(tableSV.getValueAt(row, 6).toString());
            
            btnCapNhat.setEnabled(true);
            btnXoa.setEnabled(true);
    }//GEN-LAST:event_tableSVMouseClicked
}
